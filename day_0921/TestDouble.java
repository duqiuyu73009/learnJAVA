package day_0921;

/**
 * 浮点类型
 */
public class TestDouble {
    public static void main(String[] args) {
        float a = 1.01f;
        System.out.println(a);//打印出a的值
        System.out.println("float类型的最大值" + Float.MAX_VALUE);//最大取值范围
        System.out.println("float类型的最小值" + Float.MIN_VALUE);//最小取值范围
        System.out.println("float类型的最大位数" + Float.SIZE);//位数

        double b = 1.01;
    }
}
